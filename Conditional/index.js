// Conditional statements are used to perform different actions based on different conditions

// if else statements
const a = 10;
if (a > 0) {
  console.log("Positive Number");
} else {
  console.log("Negative Number");
}

//else if
const b = 0;
if (b > 0) {
  console.log("Positive Number");
} else if (b < 0) {
  console.log("Negative Number");
} else {
  console.log("Zero");
}

